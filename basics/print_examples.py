# print() prints stuff to stdout (f.ex.: the terminal)
print("Hello world!")

# To print text from variables
a = "Hello"
b = "world!"
print(a + " " + b)

# You can also print out integers and floats
print(1)
print(3.14)

# Combination
print(f"pi is about", 3.14)

# f-string is very common to use when printing out text and variables together.
pi = 3.14
print(f"pi is about {pi}.")
